The first layer in the garden is composed of small iron scraps and iron cans (from food or beverages), aloe leaves, dry animal bones (not fresh animal bones), or broken clay pots.

The iron, aloe and bones can provide minerals to the soil as they decompose, and the broken clay aids in drainage of the garden so that it will not become flooded after storms.

Fist-sized stones can be substituted for the broken pots.

If bones are not available, they can be left out.

The cans are covered with soil about 10-20cm thick, thatching grass, and a thin layer of wood ash.

The wood ash provides the garden with potassium, while the thatching grass helps to retain moisture.

The soil helps to decompose the iron, bones, aloe, and ash, freeing the nutrients those materials contain so that the plants will be able to use them.

